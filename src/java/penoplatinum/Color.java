package penoplatinum;

/**
 *
 * @author MHGameWork
 */
public class Color {
  private final int r;
  private final int g;
  private final int b;
  public Color(int r, int g, int b)
  {
    this.r = r;
    this.g = g;
    this.b = b;
    
  }

  public int getB() {
    return b;
  }

  public int getG() {
    return g;
  }

  public int getR() {
    return r;
  }
  
  
}
